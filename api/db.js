import postgres from 'postgres';

export const sql = process.env.SUPABASE_DB_URL
  ? postgres(process.env.SUPABASE_DB_URL, { max: 1, ssl: 'require' })
  : null;

let securityTablesReady = null;

export async function ensureSecurityTables() {
  if (!sql) return;
  securityTablesReady = securityTablesReady || (async () => {
    await sql`
      create table if not exists public.game_sessions (
        sid text primary key,
        issued_at timestamptz not null,
        issued_ip text,
        used_at timestamptz,
        used_ip text
      )
    `;
    await sql`
      create table if not exists public.score_attempts (
        id bigint generated by default as identity primary key,
        ip text not null,
        name text not null,
        normalized_name text not null,
        created_at timestamptz not null default now()
      )
    `;
    await sql`
      create index if not exists score_attempts_ip_created_at_idx
      on public.score_attempts (ip, created_at desc)
    `;
    await sql`
      create index if not exists score_attempts_name_created_at_idx
      on public.score_attempts (normalized_name, created_at desc)
    `;
    await sql`alter table public.game_sessions enable row level security`;
    await sql`alter table public.score_attempts enable row level security`;
    await sql`revoke all on table public.game_sessions from anon`;
    await sql`revoke all on table public.game_sessions from authenticated`;
    await sql`revoke all on table public.score_attempts from anon`;
    await sql`revoke all on table public.score_attempts from authenticated`;
  })();
  await securityTablesReady;
}
